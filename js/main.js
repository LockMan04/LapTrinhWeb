// Toast function
function toast({ title = "", message = "", type = "success", duration = 3000 }) {
    const main = document.getElementById("toast");
    if (main) {
        const toast = document.createElement("div");

        // Auto remove toast
        const autoRemoveId = setTimeout(function () {
            main.removeChild(toast);
        }, duration + 1000);

        // Remove toast when clicked
        toast.onclick = function (e) {
            if (e.target.closest(".toast__close")) {
                main.removeChild(toast);
                clearTimeout(autoRemoveId);
            }
        };

        const icons = {
            cart: "bi bi-check-circle",
            favorate: "bi bi-heart-fill"
        };
        const icon = icons[type];
        const delay = (duration / 1000).toFixed(2);

        toast.classList.add("toast", `toast--${type}`);
        toast.style.animation = `slideInLeft ease .3s, fadeOut linear 1s ${delay}s forwards`;

        toast.innerHTML = `
                    <div class="toast__icon">
                        <i class="${icon}"></i>
                    </div>
                    <div class="toast__body">
                        <h3 class="toast__title">${title}</h3>
                        <p class="toast__msg">${message}</p>
                    </div>
                    <div class="toast__close">
                        <i class="bi bi-x"></i>
                    </div>
                `;
        main.appendChild(toast);
    }
}

function showSuccessToast() {
    toast({
        title: "Success",
        message: "You have successfully added to cart",
        type: "success",
        duration: 5000
    });
}

function showFavorateToast() {
    toast({
        title: "Success",
        message: "You have successfully added to your favorites",
        type: "favorate",
        duration: 5000
    });
}

